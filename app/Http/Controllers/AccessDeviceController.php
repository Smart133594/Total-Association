     */
    public function store(Request $request)
    {
        if($request->type == "name") {
            $name = $request->name;
            AccessDevice::where('id', $request->id)->update(['name' => $name]);
            return 1;
        }
        $val = $request->status == 'true' ? 1 : 0;
        AccessDevice::where('id', $request->id)->update(['active' => $val]);
        return 1;
    }

